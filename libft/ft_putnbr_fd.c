#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	nbr;

	nbr = 0;
	if (n < 0)
	{
		nbr = (unsigned int)(n * -1);
		ft_putchar_fd('-', fd);
	}
	else
		nbr = (unsigned int)(n);
	if (nbr >= 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putchar_fd(nbr % 10 + '0', fd);
	}
	else
		ft_putchar_fd(nbr % 10 + '0', fd);
}
